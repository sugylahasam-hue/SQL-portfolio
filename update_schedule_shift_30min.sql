-- Задача: обновление расписания всех занятий на 30 минут вперед
-- Демонстрирует умение изменять данные в таблицах с помощью UPDATE

UPDATE Timepair
SET start_pair = ADDTIME(start_pair, '00:30:00'),  -- добавляем 30 минут к началу пары
    end_pair = ADDTIME(end_pair, '00:30:00');     -- добавляем 30 минут к концу пары
